const height = 10;
const base = 4;
const perimeter = 2 * (height + base);
const area = height * base;

console.log(`The perimeter of a rectangle is twice its base (4) plus twice its height (10) which is 2 * ${height} + 2 * ${base} which is equal to`, perimeter);
console.log(`The area of a rectangle is given by multiplying the base (4) times the height  (10) which is ${height} * ${base} which is equal to`, area);
